process.on("SIGINT", () =>{
    console.log("\n\n\n~~ Interrupt Signal (^C) recieved");

    let emptyMessages = {
        messages: []
    };

    fs.writeFileSync(
        "./message-history.json", 
        JSON.stringify(emptyMessages, null, 2)
    );

    process.exit();
});